const calculator = {
  add: function(num1, num2) {
    return num1 + num2;
},
  subtraction: function(num1, num2) {
    return num1 - num2;
},
  multification: function(num1, num2) {
    return num1 * num2;
},
   division: function(num1, num2) {
    return num1 / num2;
},
}

console.log(calculator.add(20,20));
console.log(calculator.subtraction(20,20));
console.log(calculator.multification(20,20));
console.log(calculator.division(20,20));